class Weapon:
    def __init__(self, name, type, attacks, range, weapon_skill, ballistic_skill, strength, armour_penetration, damage):
        self.name = name
        self.type = type
        self.range = range
        self.attacks = attacks
        self.weapon_skill = weapon_skill
        self.ballistic_skill = ballistic_skill
        self.strength = strength
        self.armour_penetration = armour_penetration
        self.damage = damage
